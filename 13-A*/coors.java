public class coors{

    private int xcoor,ycoor, steps;

    public coors(int x, int y, int s){
	xcoor=x;
	ycoor=y;
	steps=s;
    }
    public int getX(){
	return xcoor;
    }
    public int getY(){
	return ycoor;
    }
    public int getSteps(){
	return steps;
    }

}
